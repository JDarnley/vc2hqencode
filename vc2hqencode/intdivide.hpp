template<class T> inline uint8_t ilog2(T x) {
  if (x >> 8)
    return ilog2(x >> 8) + 8;

  if (!x)
    throw std::invalid_argument("Cannot return logarithm of 0");

  return intlog2[x&0xFF];
}

template<class T> struct Divisor {
  Divisor() {}

  Divisor(const Divisor &other)
    : l   (other.l)
    , m   (other.m)
    , sh1 (other.sh1)
    , sh2 (other.sh2) {}

  Divisor(T d) {
    if (d == (1 << (8*sizeof(T) - 1)))
      l = 8*sizeof(T);
    else if ( d & (1 << (8*sizeof(T) - 1)) )
      l = 8*sizeof(T);
    else
      l = ilog2<T>(2*d - 1);

    sh1 = (l == 0)?0:1;
    sh2 = (l == 0)?0:l-1;
    m = (T)(((((uint64_t)1) << (8*sizeof(T)))*((((uint64_t)1) << l) - d))/d + 1);
  }

  uint8_t l;
  T m;
  uint8_t sh1;
  uint8_t sh2;
};

template<class T> inline T udiv(const T n, const T m, const uint8_t sh1, const uint8_t sh2) {
  const T t = (T)(((uint64_t)n*(uint64_t)m) >> (8*sizeof(T)));
  return (t + ((n - t) >> sh1)) >> sh2;
}
template<class T> inline T udiv(const T n, const T m, const uint8_t l);

template<> inline uint32_t udiv<uint32_t>(const uint32_t n, const uint32_t m, const uint8_t l) {
  const uint64_t t = (((uint64_t)n)*((uint64_t)m)) >> 32;
  return (t + n) >> l;
}

template<> inline uint16_t udiv<uint16_t>(const uint16_t n, const uint16_t m, const uint8_t l) {
  const uint32_t t = (((uint32_t)n)*((uint32_t)m)) >> 16;
  return (t + n) >> l;
}

